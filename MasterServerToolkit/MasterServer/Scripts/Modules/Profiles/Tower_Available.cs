        public void Server_Check_Tower_Level_0_To_Level_1(ObservableServerProfile profile)
        {
            string[] Shop_CodeNames = System.Enum.GetNames(typeof(Shop_Code));
            Debug.Log("Check_Tower_Level_0_To_Level_1 || " + Shop_CodeNames.Length);
            for (int i = 0; i < Shop_CodeNames.Length; i++)
            {
                if (Shop_CodeNames[i].Length > 4)
                {
                    string Name = Shop_CodeNames[i].Substring(0, 5);
                    if (Name == "Tower")
                    {
                        string Code_Level = Shop_CodeNames[i] + "_Level";
                        int Level = Get_Property_by_String(profile, Code_Level).GetValue();
                        if (Level == 0)
                        {
                            string Code_Exp = Shop_CodeNames[i] + "_EXP";
                            int EXP = Get_Property_by_String(profile, Code_Exp).GetValue();
                            if (EXP > 0 && Level == 0)
                                Get_Property_by_String(profile, Code_Level).Set(1);
                        }
                    }
                }
            }
        }

